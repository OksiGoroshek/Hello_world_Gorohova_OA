way = input('Выберете направление пути (направо, прямо, налево): ')
if way == 'направо':
    print('направу ехати — женату быти')
elif way == 'налево':
    print('налеву ехати — богату быти')
elif way == 'прямо':
    print('Как пряму ехати — живу не бывати — нет пути ни проезжему, ни прохожему, ни пролетному')
else:
    print('Направлениие пути не распознано, попробуйте в другой раз')